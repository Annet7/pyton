for i in range(len(my_list)): # создаю индексы для каждого элемента списка
    if search == my_list[i]: # если искомое значение совподает с элементом,
        count +=1 #увеличиваю счетчик на единицу
        if count == 2: #если счетчик становится равным двум, 
            print(f'Ответ {i}') #то печатаю индекс элемента списка на котором остановилась
