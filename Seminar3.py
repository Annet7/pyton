#Задайте список. Напишите программу, которая определит, 
#присутствует ли в заданном списке строк некое число.

my_list = ['vgv43gj', 'bvlivunj98', 'bgty65hjb', '0bhjb76'] #создала список

print(my_list) #вывела список на экран

search = input('Введите искомое число: ') #пользователь вводит переменную, кторорую я буду искать

for item in my_list: #прохожусь по элементам списка
    if search in item: #ищу число пользователя В строках моего списка
        print(f'число {search} входит в заданный список') 
        break #если нашла, цикл обравается
else:
    print(f'Число {search}  НЕ входит в заданный список') #если не нашла, вывожу сообщение об отсутствии искомого значения



